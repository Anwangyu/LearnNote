#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;
/*
功能描述：
求两个集合的并集

函数原型：
set_union(iterator beg1,iterator end1,iterator beg2,iterator end2,iterator dest);

求两个集合的并集
注意：两个集合必须是有序序列


*/

void myPrint(int val)
{
    cout << val << " ";
}
void test01()
{
    vector<int>v1;
    vector<int>v2;
    for(int i = 0;i < 10;i++)
    {
        v1.push_back(i);
        v2.push_back(i+5);
    }

    vector<int>vTarget;
    //目标容器开辟空间
    //最特殊的情况是两个容器没有交集，并集就是两个容器的size相加
    vTarget.resize(v1.size()+v2.size());

    vector<int>::iterator itEnd = set_union(v1.begin(),v1.end(),v2.begin(),v2.end(),vTarget.begin());
    for_each(vTarget.begin(),itEnd,myPrint);


    }

int main()
{
   test01();
    return 0;
}
